'''

    Operadores aritmeticos:

+ = soma ou concatena
- = subtração
* = multilplicação
/ = divisão
// =  divisão inteira
** = exponenciação
% = resto da divisão
() = altera a ordem de precedencia

'''

print('Multiplicação:','10 * 10 = ',10 * 10)
print('Soma:','10 + 10 = ',10 + 10)
print('Concatenando:','10 + 10 = ','10' + '10')
print('Subtração:','10 - 10 = ', 10 - 10)
print('Divisão:','10 / 10 = ',10 / 10)
print('Divisão inteira:','10 // 10 = ',10 // 10)
print('Exponenciação:','10 ** 10 = ',10 ** 10)
print('Resto da Divisão:','10 % 10 = ', 10 % 10)
print('Alterando Ordem de prescedencia:','(10/2)+20-(10*10) = ',10/2+20-10*10)
print('Alterando Ordem de prescedencia:','(10/2)+20-(10*10) = ',(10/2+20)-10*10)

print('\n','$'*80,sep='')

print('\nConcatenando:','10 + 10 = ','10' + str(10))