# Usando o sep= '' para anular o espaço que vem por padrão do python
print('Junior', 'Cardoso', 'Miguel', 'Bernardo',sep='')
# sep='' com end='' vai tirar os espaços e juntar a proxima linha (tudo oq colocar dentro das aspas vai aparecer no terminal)
print('Junior', 'Cardoso', 'Miguel', 'Bernardo',sep='',end='-')
print('Junior', 'Cardoso', 'Miguel', 'Bernardo',sep='#')
# Outros Testes
print('Junior', 'Cardoso', 'Miguel', 'Bernardo',end='')
print('Junior', 'Cardoso', 'Miguel', 'Bernardo',sep='#')